-- 3、员工表
-- ----------------------------
DROP TABLE IF EXISTS mdm_employee;

CREATE TABLE mdm_employee
(
    id                BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '员工ID',
    organization_name VARCHAR(255)      NOT NULL COMMENT '法人名称',
    organization_code VARCHAR(4)        NOT NULL COMMENT '一级编码',
    employee_name     VARCHAR(100)      NOT NULL COMMENT '员工姓名',
    employee_number   VARCHAR(5)        NOT NULL COMMENT '二级编码',
    employee_code     VARCHAR(9) UNIQUE NOT NULL COMMENT '员工编码（一级编码 + 二级编号）',
    create_by         VARCHAR(64)  DEFAULT 'admin' COMMENT '创建者',
    create_time       DATETIME     DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by         VARCHAR(64)  DEFAULT NULL COMMENT '更新者',
    update_time       DATETIME     DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    remark            VARCHAR(500) DEFAULT NULL COMMENT '备注',

    -- 外键约束，确保 `organization_code` 必须在 `mdm_organization` 表中存在
    CONSTRAINT fk_employee_organization FOREIGN KEY (organization_code) REFERENCES mdm_organization (organization_code) ON DELETE RESTRICT,

    -- 确保 `employee_code` 是 `organization_code` + `employee_number`
    CONSTRAINT chk_employee_code_format CHECK (employee_code REGEXP CONCAT(organization_code, '[0-9]{5}'))
) COMMENT ='员工表';

