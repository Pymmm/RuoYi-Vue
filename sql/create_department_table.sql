-- ----------------------------
-- 2、部门表
-- ----------------------------
DROP TABLE IF EXISTS mdm_department;

CREATE TABLE mdm_department
(
    id                BIGINT AUTO_INCREMENT PRIMARY KEY COMMENT '部门ID',
    -- 生成列，使用 STORED 使其支持索引和唯一约束
    department_code   VARCHAR(8) GENERATED ALWAYS AS (
        CONCAT(organization_code, IFNULL(unit_code, ''), IFNULL(sub_unit_code, ''))
        ) STORED UNIQUE COMMENT '部门编码',
    organization_code VARCHAR(4)   NOT NULL COMMENT '组织编码',
    organization_name VARCHAR(255) NOT NULL COMMENT '组织名称',
    unit_code         VARCHAR(2)   DEFAULT NULL COMMENT '一级编码',
    unit_name         VARCHAR(255) DEFAULT NULL COMMENT '一级单位',
    sub_unit_code     VARCHAR(2)   DEFAULT NULL COMMENT '二级编码',
    sub_unit_name     VARCHAR(255) DEFAULT NULL COMMENT '二级单位',
    create_by         VARCHAR(64)  DEFAULT 'admin' COMMENT '创建者',
    create_time       DATETIME     DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    update_by         VARCHAR(64)  DEFAULT NULL COMMENT '更新者',
    update_time       DATETIME     DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    remark            VARCHAR(500) DEFAULT NULL COMMENT '备注',

    -- 约束：organization_code 必须以 "8" 开头，后面跟 3 个数字
    CONSTRAINT chk_organization_code_format CHECK (organization_code REGEXP '^8[0-9]{3}$'),

    -- 约束：unit_code 必须是 2 位数字，允许 NULL
    CONSTRAINT chk_unit_code_format CHECK (unit_code REGEXP '^[0-9]{2}$' OR unit_code IS NULL),

    -- 约束：sub_unit_code 必须是 2 位数字，允许 NULL
    CONSTRAINT chk_sub_unit_code_format CHECK (sub_unit_code REGEXP '^[0-9]{2}$' OR sub_unit_code IS NULL)

) COMMENT ='部门表';

